package Business.Food;

/**
 *
 * @author rama
 */
public class Food {
    
    private String name;
    private int cost;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }
    
    @Override
    public String toString(){
        return this.name;
    }
    
}
