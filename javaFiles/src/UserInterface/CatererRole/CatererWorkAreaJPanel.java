    public void populateFoodJTable(){
         
        DefaultTableModel dtm = (DefaultTableModel) foodJTable.getModel();
        
        dtm.setRowCount(0);
        
        for(Food food : foodDirectory.getFoodDirectory()){
            Object[] row = new Object[2];
            row[0] = food.getName();
            row[1] = food.getCost();
            
            dtm.addRow(row);
        }
         
     }
