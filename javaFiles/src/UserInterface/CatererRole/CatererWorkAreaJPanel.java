    private void exitMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_exitMouseReleased

        try{
            
            if(foodjTextField.getText().isEmpty()||pricejTextField.getText().isEmpty()){
                throw new Exception();
            }
            
            String foodName = foodjTextField.getText();
            int foodCost = Integer.parseInt(pricejTextField.getText());
            
            if(foodCost<account.getFunds()){
                
             Food food = new Food();
             food.setName(foodName);
             food.setCost(foodCost);
             foodDirectory.setFoodDirectory(food);
             
             JOptionPane.showMessageDialog(null, "Food Item successfully added!");
             populateFoodJTable();
                
            }
            else{
             JOptionPane.showMessageDialog(null, "Insufficient funds in account. Raise Donation!");
            }
            
             
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please Enter fields Correctly!");
        }
        
       
    }//GEN-LAST:event_exitMouseReleased

